package app;

public class Student {
    public String roll;
    public String name;
    public String branch;
    public int year;
    public String email;

    public Student(String roll, String name, String branch, int year, String email) {
        this.roll = roll;
        this.name = name;
        this.branch = branch;
        this.year = year;
        this.email = email;
    }

}
